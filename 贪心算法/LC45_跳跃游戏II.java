/**
 * =====================================================================
 * LeetCode 45. 跳跃游戏 II（中等）
 * =====================================================================
 *
 * 【题目描述】
 * 给定一个长度为 n 的 0 索引整数数组 nums，初始位置在下标 0。
 * 每个元素 nums[i] 表示从索引 i 向后跳转的最大长度。
 * 换句话说，如果你在下标 i 处，可以跳到任意 (i + j) 处，其中：
 *   0 <= j <= nums[i]，且 i + j < n
 * 返回到达 n - 1 的最小跳跃次数。测试用例保证可以到达 n - 1。
 *
 * 【示例 1】
 *   输入：nums = [2,3,1,1,4]
 *   输出：2
 *   解释：从下标 0 跳 1 步到下标 1，再从下标 1 跳 3 步到最后一个位置。最少 2 次。
 *
 * 【示例 2】
 *   输入：nums = [2,3,0,1,4]
 *   输出：2
 *
 * =====================================================================
 * 【贪心思路】
 * =====================================================================
 * 核心问题：最少跳几次才能到达终点？
 *
 * 贪心策略：把每次跳跃看成一个"覆盖区间"。
 * 每次跳跃后，我们在新的覆盖范围内找下一跳能到达的最远位置。
 * 只有当我们"用完"当前这跳的覆盖范围时，才被迫再跳一次。
 *
 * 关键变量：
 *   - curEnd：当前这一跳所能到达的最远边界（当前跳跃的右边界）
 *   - farthest：在当前跳跃范围内，下一跳能到达的最远位置
 *   - jumps：跳跃次数计数器
 *
 * 遍历时：
 *   1. 不断更新 farthest = max(farthest, i + nums[i])（探索下一跳的最远边界）
 *   2. 当 i == curEnd 时，说明当前这跳走到头了，必须跳一次
 *      - jumps++
 *      - curEnd = farthest（更新为下一跳的最远边界）
 *
 * 【为什么这是贪心？】
 * 每次在当前覆盖范围内，我们贪心地选择"下一跳能飞最远"的位置。
 * 这样每次跳跃都物尽其用，保证跳跃次数最少。
 * =====================================================================
 */
public class LC45_跳跃游戏II {

    /**
     * 贪心算法求最少跳跃次数
     * 时间复杂度：O(n)，只遍历一次数组
     * 空间复杂度：O(1)，只用了固定数量的变量
     *
     * @param nums 每个位置可以跳跃的最大步数
     * @return 到达最后一个位置的最少跳跃次数
     */
    public static int jump(int[] nums) {
        // 特殊情况：只有一个元素，已经在终点，不需要跳
        if (nums.length == 1) return 0;

        // jumps：已经跳了多少次（最终答案）
        int jumps = 0;

        // curEnd：当前跳跃所能覆盖的最远下标（当前这一跳的边界）
        // 初始在 0，因为我们从下标 0 出发
        int curEnd = 0;

        // farthest：在当前覆盖范围内，下一跳能到达的最远下标
        // 初始为 0
        int farthest = 0;

        // 遍历从 0 到倒数第二个位置（不需要从终点起跳）
        for (int i = 0; i < nums.length - 1; i++) {

            // 探索：从位置 i 起跳，最远能到 i + nums[i]
            // 不断用它更新下一跳的最远边界
            farthest = Math.max(farthest, i + nums[i]);

            // 关键判断：当前位置 i 恰好到达了本次跳跃的边界
            // 说明当前这一跳已经走完，必须触发下一次跳跃
            if (i == curEnd) {
                // 跳跃次数加 1
                jumps++;

                // 将下一跳的覆盖边界更新为 farthest
                // （贪心：选当前范围内下一跳最远的那个点）
                curEnd = farthest;

                // 小优化：如果当前覆盖已经能到达终点，不必继续遍历
                if (curEnd >= nums.length - 1) {
                    break;
                }
            }
        }

        return jumps;
    }

    // =====================================================================
    // main 方法：运行测试用例，验证算法正确性
    // =====================================================================
    public static void main(String[] args) {

        // 测试用例 1：官方示例，期望输出 2
        int[] test1 = {2, 3, 1, 1, 4};
        System.out.println("测试1：" + jump(test1));  // 期望：2

        // 测试用例 2：官方示例，期望输出 2
        int[] test2 = {2, 3, 0, 1, 4};
        System.out.println("测试2：" + jump(test2));  // 期望：2

        // 测试用例 3：只有一个元素，不需要跳，期望输出 0
        int[] test3 = {0};
        System.out.println("测试3：" + jump(test3));  // 期望：0

        // 测试用例 4：每步只能走 1，需要走 n-1 步，期望输出 4
        int[] test4 = {1, 1, 1, 1, 1};
        System.out.println("测试4：" + jump(test4));  // 期望：4

        // 测试用例 5：第一步就能到终点，期望输出 1
        int[] test5 = {10, 1, 1, 1, 1};
        System.out.println("测试5：" + jump(test5));  // 期望：1

        // 测试用例 6：两步跳到终点，期望输出 2
        // 下标 0 跳到下标 1，再从 1 跳到 5
        int[] test6 = {1, 5, 0, 0, 0, 0};
        System.out.println("测试6：" + jump(test6));  // 期望：2
    }
}
