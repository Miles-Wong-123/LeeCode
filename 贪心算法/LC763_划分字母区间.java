import java.util.ArrayList;
import java.util.List;

/**
 * =====================================================================
 * LeetCode 763. 划分字母区间（中等）
 * =====================================================================
 *
 * 【题目描述】
 * 给你一个字符串 s。我们要把这个字符串划分为尽可能多的片段，
 * 同一字母最多出现在一个片段中。
 * 注意，划分结果需要满足：将所有划分结果按顺序连接，得到的字符串仍然是 s。
 * 返回一个表示每个字符串片段的长度的列表。
 *
 * 【示例 1】
 *   输入：s = "ababcbacadefegdehijhklij"
 *   输出：[9, 7, 8]
 *   解释：划分结果为 "ababcbaca"、"defegde"、"hijhklij"。
 *         每个字母最多出现在一个片段中。
 *
 * 【示例 2】
 *   输入：s = "eccbbbdec"
 *   输出：[10]
 *   解释：无法在中间切割，因为 'e' 和 'c' 的最后出现位置在末尾。
 *
 * =====================================================================
 * 【贪心思路】
 * =====================================================================
 * 核心约束：同一个字母只能出现在同一个片段里。
 *
 * 因此，一个片段的结束位置至少要到"该片段内所有字母最后一次出现的位置"。
 *
 * 贪心策略分两步走：
 *
 * 第一步（预处理）：记录每个字母在字符串中最后一次出现的下标。
 *   例如 "ababcbaca" 中，'a' 最后出现在下标 8，'b' 最后出现在下标 5，'c' 最后出现在下标 7。
 *
 * 第二步（贪心切割）：用一个"当前片段的结束位置" end 变量，
 *   遍历字符串时，不断用"当前字母的最后出现位置"来扩展 end。
 *   当遍历到 end 时，说明当前片段内所有字母都已完整包含，可以切割！
 *
 * 【为什么这是贪心？】
 * 我们每次都尽量让片段尽早结束（能切就切），这样片段数最多。
 * 同时，片段边界由字母最后出现位置决定，保证了约束不被违反。
 * =====================================================================
 */
public class LC763_划分字母区间 {

    /**
     * 贪心算法划分字母区间
     * 时间复杂度：O(n)，遍历两次字符串
     * 空间复杂度：O(1)，last 数组大小固定为 26（字母表大小），与输入无关
     *
     * @param s 仅由小写英文字母组成的字符串
     * @return 每个片段的长度列表
     */

    public static List<Integer> partitionLabels(String s) {
        // 第一步：预处理——记录每个字母最后一次出现的下标
        // last[0] 对应 'a'，last[1] 对应 'b'，...，last[25] 对应 'z'
        int[] last = new int[26];
        for (int i = 0; i < s.length(); i++) {
            // s.charAt(i) - 'a' 将字符转换为 0-25 的索引
            // 遍历完后，last[c] 就是字母 c 最后出现的位置
            last[s.charAt(i) - 'a'] = i;
        }

        // 存储每个片段长度的结果列表
        List<Integer> result = new ArrayList<>();

        // start：当前片段的起始下标
        int start = 0;

        // end：当前片段必须延伸到的最远下标（由片段内字母决定）
        int end = 0;

        // 第二步：贪心遍历，确定每个片段的边界
        for (int i = 0; i < s.length(); i++) {
            // 当前字母是 s.charAt(i)，它最后出现在 last[s.charAt(i) - 'a']
            // 为了让这个字母只出现在当前片段，end 至少要到它最后出现的位置
            
            end = Math.max(end, last[s.charAt(i) - 'a']);

            // 关键判断：当遍历位置 i 恰好到达当前片段的边界 end
            // 说明当前片段内的所有字母的最后出现位置都在 end 以内
            // 可以在这里切割！
            if (i == end) {
                // 当前片段的长度 = end - start + 1
                result.add(end - start + 1);

                // 下一个片段从 i+1 开始
                start = i + 1;
            }
        }

        return result;
    }

    // =====================================================================
    // main 方法：运行测试用例，验证算法正确性
    // =====================================================================
    public static void main(String[] args) {

        // 测试用例 1：官方示例，期望输出 [9, 7, 8]
        String test1 = "ababcbacadefegdehijhklij";
        System.out.println("测试1：" + partitionLabels(test1));  // 期望：[9, 7, 8]

        // 测试用例 2：官方示例，'e' 出现在 0,7，'c' 出现在 1,2,8，无法在中间切割
        // 整个 9 字符字符串是一个片段，期望输出 [9]
        String test2 = "eccbbbdec";
        System.out.println("测试2：" + partitionLabels(test2));  // 期望：[9]

        // 测试用例 3：每个字母只出现一次，可以逐个切割，期望输出 [1, 1, 1]
        String test3 = "abc";
        System.out.println("测试3：" + partitionLabels(test3));  // 期望：[1, 1, 1]

        // 测试用例 4：只有一种字符，整体是一个片段，期望输出 [5]
        String test4 = "aaaaa";
        System.out.println("测试4：" + partitionLabels(test4));  // 期望：[5]

        // 测试用例 5：两种字符交叉出现，必须合并，期望输出 [4]
        // a 最后出现在 3，b 最后出现在 2，所以整体不可分割
        String test5 = "abba";
        System.out.println("测试5：" + partitionLabels(test5));  // 期望：[4]

        // 测试用例 6：分成两段，期望输出 [2, 2]
        // "ab" 中 a 最后在 0，b 最后在 1 → 第一段 [0,1]
        // "cd" 中 c 最后在 2，d 最后在 3 → 第二段 [2,3]
        String test6 = "abcd";
        System.out.println("测试6：" + partitionLabels(test6));  // 期望：[1, 1, 1, 1]
    }
}
