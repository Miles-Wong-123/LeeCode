/**
 * =====================================================================
 * LeetCode 55. 跳跃游戏（中等）
 * =====================================================================
 *
 * 【题目描述】
 * 给你一个非负整数数组 nums，你最初位于数组的第一个下标。
 * 数组中的每个元素代表你在该位置可以跳跃的最大长度。
 * 判断你是否能够到达最后一个下标，如果可以，返回 true；否则，返回 false。
 *
 * 【示例 1】
 *   输入：nums = [2,3,1,1,4]
 *   输出：true
 *   解释：先从下标 0 跳 1 步到下标 1，再从下标 1 跳 3 步到最后一个下标。
 *
 * 【示例 2】
 *   输入：nums = [3,2,1,0,4]
 *   输出：false
 *   解释：无论如何都会到达下标 3，但该位置的最大跳跃长度是 0，无法继续前进。
 *
 * =====================================================================
 * 【贪心思路】
 * =====================================================================
 * 核心问题：能否到达最后一个位置？
 *
 * 贪心策略：始终维护一个变量 maxReach，表示"当前能到达的最远位置"。
 *
 * 遍历数组时：
 *   1. 如果当前位置 i > maxReach，说明连当前位置都到不了，更别说终点，直接返回 false。
 *   2. 否则，从当前位置 i 出发，最远能跳到 i + nums[i]，
 *      用它更新 maxReach = max(maxReach, i + nums[i])。
 *   3. 遍历结束后，能走完整个循环说明每个位置都可达，返回 true。
 *
 * 【为什么这是贪心？】
 * 我们不关心具体怎么跳，只关心"当前最远能覆盖到哪里"。
 * 每一步都尽量扩大覆盖范围，这就是贪心的体现。
 * =====================================================================
 */
public class LC55_跳跃游戏 {

    /**
     * 贪心算法解决跳跃游戏
     * 时间复杂度：O(n)，只需遍历一次数组
     * 空间复杂度：O(1)，只用了一个额外变量
     *
     * @param nums 每个位置可以跳跃的最大步数
     * @return 是否能到达最后一个位置
     */
    public static boolean canJump(int[] nums) {
        // maxReach 表示当前能到达的最远下标（贪心核心变量）
        // 初始在下标 0，所以最远能到下标 0
        int maxReach = 0;

        // 遍历数组中的每一个位置
        for (int i = 0; i < nums.length; i++) {

            // 关键判断：如果当前位置 i 已经超出了我们能到达的最远位置
            // 说明下标 i 根本无法被访问到，也就无法到达终点
            if (i > maxReach) {
                return false;
            }

            // 从位置 i 出发，最远可以跳到 i + nums[i]
            // 贪心：用这个值更新全局最远可达位置
            maxReach = Math.max(maxReach, i + nums[i]);

            // 小优化：如果最远已经能覆盖到终点，提前返回
            if (maxReach >= nums.length - 1) {
                return true;
            }
        }

        // 正常走完循环，说明所有位置都可达，返回 true
        return true;
    }

    // =====================================================================
    // main 方法：运行测试用例，验证算法正确性
    // =====================================================================
    public static void main(String[] args) {

        // 测试用例 1：官方示例，期望输出 true
        int[] test1 = {2, 3, 1, 1, 4};
        System.out.println("测试1：" + canJump(test1));   // 期望：true

        // 测试用例 2：官方示例，期望输出 false
        int[] test2 = {3, 2, 1, 0, 4};
        System.out.println("测试2：" + canJump(test2));   // 期望：false

        // 测试用例 3：只有一个元素，不需要跳，直接在终点，期望 true
        int[] test3 = {0};
        System.out.println("测试3：" + canJump(test3));   // 期望：true

        // 测试用例 4：起点就是 0，无法移动，期望 false
        int[] test4 = {0, 1, 2};
        System.out.println("测试4：" + canJump(test4));   // 期望：false

        // 测试用例 5：第一步直接跳到终点，期望 true
        int[] test5 = {5, 0, 0, 0, 0};
        System.out.println("测试5：" + canJump(test5));   // 期望：true
    }
}
