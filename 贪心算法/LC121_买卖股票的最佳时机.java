/**
 * =====================================================================
 * LeetCode 121. 买卖股票的最佳时机（简单）
 * =====================================================================
 *
 * 【题目描述】
 * 给定一个数组 prices，它的第 i 个元素 prices[i] 表示一支股票第 i 天的价格。
 * 你只能选择某一天买入这只股票，并选择在未来的某一个不同的日子卖出该股票。
 * 设计一个算法来计算你所能获取的最大利润。
 * 返回你可以从这笔交易中获取的最大利润。如果你不能获取任何利润，返回 0。
 *
 * 【示例 1】
 *   输入：prices = [7,1,5,3,6,4]
 *   输出：5
 *   解释：在第 2 天（价格=1）买入，在第 5 天（价格=6）卖出，利润 = 6-1 = 5。
 *
 * 【示例 2】
 *   输入：prices = [7,6,4,3,1]
 *   输出：0
 *   解释：价格一直下跌，没有交易能盈利，返回 0。
 *
 * =====================================================================
 * 【贪心思路】
 * =====================================================================
 * 核心问题：找一个"低买高卖"的最大差值，且买入必须在卖出之前。
 *
 * 贪心策略：一边遍历价格，一边维护"历史最低买入价"。
 *
 * 对于第 i 天，如果我们今天卖出，最大利润 = prices[i] - 历史最低价格。
 * 我们只需要记录遍历过程中的最小价格（最佳买入点），
 * 同时更新最大利润即可。
 *
 * 关键点：因为是从左往右遍历，"历史最低价"天然保证了买入早于卖出。
 *
 * 【为什么这是贪心？】
 * 每天的决策是：今天卖出的利润 = 今天价格 - 迄今为止最低价格。
 * 我们贪心地选择"用最低的历史价格买入"，这样每天的潜在利润最大化。
 * =====================================================================
 */
public class LC121_买卖股票的最佳时机 {

    /**
     * 贪心算法求最大股票利润
     * 时间复杂度：O(n)，只遍历一次
     * 空间复杂度：O(1)，只用了两个额外变量
     *
     * @param prices 每天的股票价格
     * @return 最大利润，若无法盈利则返回 0
     */

    public static int maxProfit(int[] prices) {
        // minPrice：记录遍历到目前为止的历史最低价格（贪心：选最便宜的时机买入）
        // 初始化为最大整数，确保第一天的价格一定会被选中
        int minPrice = Integer.MAX_VALUE;

        // maxProfit：记录目前遇到的最大利润
        // 初始化为 0，表示"不交易"时利润为 0
        int maxProfit = 0;

        // 逐天遍历股票价格
        for (int price : prices) {

            // 贪心决策一：如果今天价格比历史最低价还低，更新最低买入价
            // （意味着今天是更好的买入时机）
            if (price < minPrice) {
                minPrice = price;
            } else {
                // 贪心决策二：今天价格高于历史最低，计算"今天卖出"的利润
                // 用当天利润更新最大利润
                int profit = price - minPrice;
                maxProfit = Math.max(maxProfit, profit);
            }

            // 注意：以上两个 if-else 也可以合并写为：
            // minPrice = Math.min(minPrice, price);
            // maxProfit = Math.max(maxProfit, price - minPrice);
            // 这里分开写是为了让逻辑更清晰
        }

        // 遍历结束，maxProfit 就是最大利润
        // 若股价一直下跌，maxProfit 始终为 0，符合题意
        return maxProfit;
    }


    // =====================================================================
    // main 方法：运行测试用例，验证算法正确性
    // =====================================================================
    public static void main(String[] args) {

        // 测试用例 1：官方示例，期望输出 5
        int[] test1 = {7, 1, 5, 3, 6, 4};
        System.out.println("测试1：" + maxProfit(test1));  // 期望：5

        // 测试用例 2：官方示例，价格单调递减，期望输出 0
        int[] test2 = {7, 6, 4, 3, 1};
        System.out.println("测试2：" + maxProfit(test2));  // 期望：0

        // 测试用例 3：只有一天，无法交易，期望输出 0
        int[] test3 = {5};
        System.out.println("测试3：" + maxProfit(test3));  // 期望：0

        // 测试用例 4：价格先跌后涨，期望输出 4（1 买 5 卖）
        int[] test4 = {3, 2, 1, 5, 4};
        System.out.println("测试4：" + maxProfit(test4));  // 期望：4

        // 测试用例 5：价格持续上涨，期望输出 5（1 买 6 卖）
        int[] test5 = {1, 2, 3, 4, 5, 6};
        System.out.println("测试5：" + maxProfit(test5));  // 期望：5
    }
}
